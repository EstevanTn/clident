namespace Application\Model\Entity;


class Medicacion extends Medicamento
{
    var $ID_MEDICACION;
    var $ID_DETALLE_ODONTOGRAMA;
    var $DESCRIPCION_MEDICACION;
    public function exchangeArray(array $data)
    {
        parent::exchangeArray($data); // TODO: Change the autogenerated stub
        $this->ID_MEDICACION = !empty($data['ID_MEDICACION']) ? $data['ID_MEDICACION'] : null;
        $this->ID_DETALLE_ODONTOGRAMA = !empty($data['ID_DETALLE_ODONTOGRAMA']) ? $data['ID_DETALLE_ODONTOGRAMA'] : null;
        $this->DESCRIPCION_MEDICACION = !empty($data['DESCRIPCION_MEDICACION']) ? $data['DESCRIPCION_MEDICACION'] : null;
    }
}