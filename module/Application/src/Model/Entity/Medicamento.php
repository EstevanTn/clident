namespace Application\Model\Entity;


class Medicamento extends Marca
{
    var $ID_MEDICAMENTO;
    var $NOMBRE;
    var $DESCRIPCION;

    public function exchangeArray(array $data)
    {
        parent::exchangeArray($data); // TODO: Change the autogenerated stub
        $this->ID_MEDICAMENTO = !empty($data['ID_MEDICAMENTO']) ? $data['ID_MEDICAMENTO'] : null;
        $this->NOMBRE = !empty($data['NOMBRE']) ? $data['NOMBRE'] : null;
        $this->DESCRIPCION = !empty($data['DESCRIPCION']) ? $data['DESCRIPCION'] : null;
    }

    public static function getColumnNames()
    {
        return [
            'ID_MEDICAMENTO',
            'NOMBRE',
            'DESCRIPCION'
        ];
    }
}